/**
 * Lists images from each Cloudinary Media Library folder (asset_folder)
 * and writes src/data/galleries.generated.ts
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { v2 as cloudinary } from 'cloudinary';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

const CATEGORY_FOLDERS = [
  { slug: 'wedding', folder: 'Wedding', title: 'Wedding Photography' },
  { slug: 'maternity', folder: 'Maternity', title: 'Maternity Shoot' },
  { slug: 'baby-photoshoot', folder: 'Baby Photoshoot', title: 'Baby Photoshoot' },
  { slug: 'birthday', folder: 'Birthday', title: 'Birthday' },
  { slug: 'family-photoshoot', folder: 'Family Photoshoot', title: 'Family Photoshoot' },
  { slug: 'celebrity', folder: 'Celebrity', title: 'Celebrity' },
];

const EXCLUDED = new Set(['samples', 'sample']);

function loadEnv() {
  const envPath = path.join(root, '.env');
  if (!fs.existsSync(envPath)) return;
  for (const line of fs.readFileSync(envPath, 'utf8').split('\n')) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const i = t.indexOf('=');
    if (i === -1) continue;
    const key = t.slice(0, i).trim();
    let val = t.slice(i + 1).trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    if (!process.env[key]) process.env[key] = val;
  }
}

/** Media Library folders use asset_folder, not public_id prefixes */
async function listImagesByAssetFolder(folderName) {
  const resources = [];
  let next_cursor;

  do {
    let query = cloudinary.search
      .expression(`asset_folder="${folderName}"`)
      .sort_by('created_at', 'desc')
      .max_results(500);
    if (next_cursor) query = query.next_cursor(next_cursor);
    const result = await query.execute();
    resources.push(...(result.resources ?? []));
    next_cursor = result.next_cursor;
  } while (next_cursor);

  return resources.filter((r) => {
    const folder = (r.asset_folder ?? '').toLowerCase();
    return !EXCLUDED.has(folder) && !r.public_id.toLowerCase().includes('/samples/');
  });
}

function esc(s) {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

async function main() {
  loadEnv();

  const cloud = process.env.PUBLIC_CLOUDINARY_CLOUD_NAME;
  const key = process.env.CLOUDINARY_API_KEY;
  const secret = process.env.CLOUDINARY_API_SECRET;

  if (!cloud || !key || !secret) {
    console.error(`
Missing Cloudinary credentials in .env:

  PUBLIC_CLOUDINARY_CLOUD_NAME=
  CLOUDINARY_API_KEY=
  CLOUDINARY_API_SECRET=
`);
    process.exit(1);
  }

  cloudinary.config({ cloud_name: cloud, api_key: key, api_secret: secret });

  const galleries = [];

  for (const cat of CATEGORY_FOLDERS) {
    console.log(`Fetching folder: ${cat.folder}`);
    const images = await listImagesByAssetFolder(cat.folder);
    console.log(`  → ${images.length} image(s)`);

    const photos = images.map((img, i) => ({
      src: img.public_id,
      alt: `${cat.title} — photo ${i + 1}`,
      width: img.width || 1600,
      height: img.height || 1067,
    }));

    galleries.push({ categorySlug: cat.slug, photos });
  }

  const out = `// AUTO-GENERATED by npm run sync:galleries — do not edit by hand
// Generated: ${new Date().toISOString()}

import type { Gallery } from './galleries';

export const galleriesGenerated: Gallery[] = [
${galleries
  .map(
    (g) => `  {
    categorySlug: '${g.categorySlug}',
    photos: [
${g.photos.map((p) => `      { src: '${esc(p.src)}', alt: '${esc(p.alt)}', width: ${p.width}, height: ${p.height} },`).join('\n')}
    ],
  },`,
  )
  .join('\n')}
];
`;

  const outPath = path.join(root, 'src/data/galleries.generated.ts');
  fs.writeFileSync(outPath, out, 'utf8');
  console.log(`\nWrote ${outPath}`);
  console.log('Next: git add src/data/galleries.generated.ts && git commit && git push');
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
